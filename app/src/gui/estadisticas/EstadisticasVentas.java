    private void setTablaVentas(List<DtoVentas> miLista){
        vaciarTabla(jtVentas);
        Integer total = 0;
        String v[] = new String[6];
        DateFormat dateFormatter;
        dateFormatter = DateFormat.getDateInstance(DateFormat.SHORT, Locale.UK);
        
        for(int i = 0; i < miLista.size(); i++){
            v[0] = miLista.get(i).getIdVenta().toString();
            v[1] = miLista.get(i).getFkMesa().getIdMesa().toString();
            v[2] = miLista.get(i).getFkEstado().getDescripcion();
            v[3] = dateFormatter.format(miLista.get(i).getFecha()).toString();
            v[4] = miLista.get(i).getFkUsuario().getNombreUsuario();
            v[5] = miLista.get(i).getTotal().toString();
            modelo.addRow(v);
            total = total + miLista.get(i).getTotal();
        }
        lblTotal.setText(total.toString());
        revalidate();
        
    }
    
    private void vaciarTabla(JTable tabla) {
        try {
            modelo = (DefaultTableModel) tabla.getModel();
            int filas = tabla.getRowCount();
            for (int i = 0; filas > i; i++) {
                modelo.removeRow(0);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al limpiar la tabla.");
        }
    }
    
